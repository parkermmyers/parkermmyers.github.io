			<p>Whole was a very fun project for me, and a great experience working with a client that had a strong collection of inspiration but also put a lot of trust in me to execute. Whole is a blanket company focused on creating the ultimate blanket. The concept is still in production, but their team came to me with some logo ideas and general inspiration and said "have at it".</br></br>
			The guiding principles for this logo were "warm and clean", which felt very appropriate for the product. We utilized a chunky serif typeface for the branding that reminded me a lot of Cooper Black, but with a little less playfulness and a little more sophistication (Cooper Black is a personal favorite though). We wanted to create a wordmark for the main logo and a separate icon for social media that was visually connected but not identical. We pulled out a "W" and dropped it into an abstract sort of hole, a hint towards how this product will be differentiated. This borders on a little esoteric, but the hole functions as sort of a hug for the W, echoing those themes of warmth and comfort.</br></br>
			We decided to hold off on finalizing a color palette until the prototypes of the blanket were finished, but for now we have a warm grayscale palette that feels very calming. All together, we have a soft and clean mark surrounded by a very simple and subdued brand. 
